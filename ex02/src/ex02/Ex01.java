package ex02;

public class Ex01 {
	
	public static void doA() {
		int c = 30;//c는 30
		int d = 40;//d는 40
		System.out.println(c+d);// c+d의 값을 출력
	}

	public static void main(String[] args) {
	
		int a = 10; //a는 10
		int b = 20; //b는 20
		System.out.println(a+b); //a+b를 출력
		
		doA();//doA는 나중에 배운다고 함.
		System.out.println("종료됩니다."); // 종료됩니다 출력
	}
	
	
}
