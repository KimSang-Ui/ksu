package ex02;

public class Ex03 {
	
	public static void main(String[] args) {
		
		// 정수 선언
		short k = 20;
		int a = 10;
		long f = 30;
		// 실수 선언
		float b = 20.23f;
		double c = 20.223d;
		
		// 문자 문자열 선언
		char d = 'A';
		String e = "안녕하세요";
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(k);
		
	}

}
// 정수: 소수점이 없는 숫자
// 실수: 소수점이 있는 숫자
// 문자열: char는 한 개의 문자를 저장할 수 있다. String은 여러 개의 문자를 저장할 수 있다.