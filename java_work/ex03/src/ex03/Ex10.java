package ex03;

public class Ex10 {

	public static void main(String[] args) {
		int a = 10;//int a = 10
		int b = 20;//int b = 20
		System.out.printf(" a = %d , b = %d\n", a, b); // a,b의 값을 출력해라 a=10 b=20 출력

		int c = 300;// int c=300
		byte d = (byte) c; //int형을 byte형으로 형변환 
		System.out.printf(" c = %d , d = %d", c, d);// c, d의 갑을 출력해라 c=300 d= 44 출력
	}
}
