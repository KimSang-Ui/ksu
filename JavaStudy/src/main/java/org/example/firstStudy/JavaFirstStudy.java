package org.example.firstStudy;

public class JavaFirstStudy // 클래스 이름
{ // 중괄호를 이용해서 클래스와 메소드의 영역을 구분
    public static void main(String[] args) // 메소드 이름
    { // 프로그램 실행 시 main 메소드 안 문장들 순차적 실행

        System.out.println("Welcome to JavaStudy");// 문장의 끝에는 세미클론을 붙여서 문장의 끝 표시
        System.out.println("JavaFirstStudy");// System.out.println의 괄호 안에 출력 내용 큰따옴표로 묶어서 표시
        // System.out.println 실행 이후 자동 줄바꿈(개행)
    }
}