package org.example.type;

public class Boolean_Type {
    public static void main(String[] args) {
        boolean b1 = true;
        boolean b2 = false;
        System.out.println(b1);
        System.out.println(b2);

        int num1 = 15;
        int num2 = 85;
        System.out.println(num1 > num2);
        System.out.println(num1 < num2);
    }
}
